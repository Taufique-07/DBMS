CREATE TABLE IF NOT EXISTS INFO (
    ID INT PRIMARY KEY,
    NAME VARCHAR(50) NOT NULL,
    AGE INT NOT NULL CHECK (AGE >= 18),
    SECTION VARCHAR(5) DEFAULT "65_B",
    GRADUATION_DATE DATE NOT NULL CHECK (GRADUATION_DATE >= '2023-01-01'),
    REGISTRATION_TIME TIME NOT NULL,
    GRADUATION_DATETIME DATETIME UNIQUE
);

